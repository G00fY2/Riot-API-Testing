package pojo_league;

public class MiniSeries {
	
	public int losses; // Number of current losses in the mini series.
	public String progress; // String showing the current, sequential mini series progress where 'W' represents a win, 'L' represents a loss, and 'N' represents a game that hasn't been played yet.
	public int target; // Number of wins required for promotion.
	public int wins; // Number of current wins in the mini series.

}
